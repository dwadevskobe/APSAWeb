            <h1>A Look into Ourstory (Updated as of January 7, 2014)</h1>
            <p> <span style="font-weight:bold">1970:</span> Asian American Student Alliance (AASA) was founded by 2 Asian American female students (Phyllis Chiu and Marlene Tuyay Scott) from what was known as Third College. </p>
            <p> <span style="font-weight:bold"> 1970s: </span> primarily served the needs of the small Asian American population at UCSD, when Affirmative action efforts only targeted Pilipinos throughout the UC system.  So, when the Student Affirmative Action Committee (SAAC) was established, AASA was named a founding or along with MEChA, BSU, & NASA, to serve the needs of underrepresented students and discuss pertinent issues. </p>
            <p><span style="font-weight:bold"> 1972:</span> First publication called AASA Supplement states its goals as: “…to promote Asian-American identity, cultural awareness, and to re-instill a sense of community and recognition of common purpose and common circumstance.” </p>
            <div class="center"><h3> Goal: </h3> </div>
            <p> To provide a forum for Asian-American self definition and expression on issues relevant to problems and needs of our community… to gain self-knowledge and Asian awareness by our collective efforts, with Asians, on all levels, to build our own true identity as Asian Americans. <br><br>

            “We resent the good ‘oriental’ … We promote the concept of pluralism … We are also diverse … We all must ‘get it together’ within our own groups…” <br><br>

            Major struggle through the years: to establish an Asian American Studies curriculum at UCSD.  Eventually with the help of other politically active ethnic organizations (MEChA, BSU, and NASA) and faculty support, an Ethnic Studies department was founded. </p>

            <p><span style="font-weight:bold;"> 1978:</span> AASA helped create statewide network of Asian & Pacific-Islander student orgs, named APSU (the Asian & Pacific-Islander Student Union), after the Bakke vs. UC Regents decision cut much needed affirmative action programs. <br><br>

            For 16 years, APSU and SAAC played important roles in shaping the direction and purpose of AASA </p>

            <p><span style="font-weight:bold"> 1980s:</span> hallmark for some of the greatest years of development and activism of AASA. </p>

            <p><span style="font-weight:bold"> 1986:</span> to reflect the change in API student populations, AASA changed its name to the Asian and Pacific-Islander Student Alliance (APSA). <br><br>
            With BSU, statewide MEChA, and NASU, organized the largest march at Sacramento since the Vietnam War. </p>

            <p><span style="font-weight:bold"> 1980s-early 1990s: </span> Bulk of students from “tidal wave” of immigration entered the 1980s-early 1990s.  Filipinos taken off the affirmative action list at UCSD and statewide b/c they were deemed as “overrepresented” </p>

            <p><span style="font-weight:bold"> 1987: </span> The Filipino-dominated leadership of APSA migrated to Kaibigang Pilipino (KP). Other ethnic organizations were in competition with APSA, but rarely did any of them tackle the more important political issues. </p>

            <p><span style="font-weight:bold"> 1990s: </span> Gender dynamic of APSA’s leadership came under critique during the early 90s.  The conflict resulted in the resignation of both chairs in the middle of the year and the formation of Sisters Across the Waves—an organization for women of API heritage. <br><br>
             APSA’s role in SAAC criticized.  KP already represented Pilipinos @ UCSD in terms of retention.  KP had replaced APSA.  But APSA changed its scope and its target populations.  Southeast Asians and Pacific-Islanders. <br><br> 
             The possibility of losing a space in SAAC came at a time when APSA was low in membership.  The leaders of the time thought that it was important that future members of APSA should retain a sense of political activism and direction in-line with SAAC.</p>
            <div class="center"><h3> Mission Statement </h3> </div>
            <p> We the students of University of California at San Diego, come together to provide a forum to deal with the problems and concerns of all UCSD Asian & Pacific-Islander (API) students especially those deemed “underrepresented” by the Student Affirmative Action Committee (SAAC); to create an environment where political, educational, cultural, and social issues can be addressed; to increase awareness about our API history and heritage within the University and the community; to unite as a community to represent a common voice thereby empowering ourselves; and to serve as a basis for building bonds and addressing differences between students. </p>

            <p><span style="font-weight:bold"> April 1995: </span> First annual APSA High School Conference, targeting at risk Asian youth that lasted an entire day.  The theme: “Taking it to the next level.”  The concept stemming from a sophomore Melany dela Cruz.<br><br>
            “To win the hearts & minds of the people, forget the dry facts and statistics; tell them the stories that won you to the cause.” </p>

            <p><span style="font-weight:bold"> 1997: </span> APSA’s mentor and mentee program was created to provide a one on one interaction between a “veteran” APSA member and a new APSA member. </p>

            <div class="center"><h3> Timeline of Events </h3> </div>
            <p><span style="font-weight:bold"> Naturalization act of 1790 </span><br>
             &bull;&nbsp; Citizenship limited to "free white persons" <br>
             &bull;&nbsp; "any alien, being a free white person, may be admitted to become a citizen of the United State" <br></p>

             <p><span style="font-weight:bold"> 1875: </span> Supreme Court declared that regulation of US immigration is the responsibility of the Federal Government. </p>

             <p><span style="font-weight:bold"> 1882: </span> The Chinese Exclusion Act: Prohibited certain laborers from immigrating to the United States. </p>

             <p><span style="font-weight:bold"> 1885 and 1887: </span> Alien Contract Labor laws which prohibited certain laborers from immigrating to the United States. </p>

             <p><span style="font-weight:bold"> 1891: </span> The Federal Government assumed the task of inspecting, admitting, rejecting, and processing all immigrants seeking admission to the U.S </p>

             <p><span style="font-weight:bold"> 1892: </span> On January 2, a new Federal US immigration station opened on Ellis Island in New York Harbor </p>

             <p><span style="font-weight:bold"> 1907: </span> The US immigration Act of 1907: Reorganized the states bordering Mexico (Arizona, New Mexico and a large part of Texas) into Mexican Border District to stem the flow of immigrants into the United States </p>

             <p><span style="font-weight:bold"> 1917-1924: </span> A series of laws were enacted to further limit the number of new immigrants. These laws established the quota system and imposed passport requirements. They expanded the categories of excludable aliens and banned all Asians except Japanese <br><br>
             The Emergency Quota Act of 1921 restricted immigration from a given country to 3% of the number of people from that country living in the U.S. in 1910<br><br>
             Ozawa v. U.S 1922 Naturalization was denied to a Japanese born man who was raised in America; 'white' was defined as 'Caucasian' <br><br>
             The Immigration Act of 1924 was aimed at further restricting the Southern and Eastern Europeans who had begun to enter the country in large numbers beginning in the 1890s <br><br>
             US vs Thind 1932 Clarified that ‘white’ as intended by the writers of the original 1790 statue meant the popular definition, not the scientific anthropological sense of ‘Aryan.’ Therefore, Thind being Punjabi (Indo-Aryan) was denied naturalization.</p>

            <p><span style="font-weight:bold"> Tydings-McDuffie Act of 1934 </span><br>
             &bull;&nbsp; Exclusion of Filipinos <br>
             &bull;&nbsp; Quota = 50 a year <br>
             &bull;&nbsp; Filipinos already in US re-classified as 'aliens' <br></p>

              <p><span style="font-weight:bold"> 1940: </span> The Alien Registration Act: Required all aliens (non-U.S. citizens) within the United States to register with the Government and receive an Alien Registration Receipt Card (the predecessor of the "Green Card").</p>

              <p><span style="font-weight:bold"> 1950: </span> Passage of the Internal Security Act: Rendered the Alien Registration Receipt Card even more valuable. Immigrants with legal status had their cards replaced with what generally became known as the "green card" (Form I-151).</p>

               <p><span style="font-weight:bold"> Immigrant Act 1965 Hart-Cellar Act </span> <br><br>
                  The 1965 act abolished 'national origin' quotas and specified seven preferences for Eastern Hemisphere quota immigrants: <br><br>

                  (1) unmarried adult sons and daughters of citizens<br><br>
                  (2) spouses and unmarried sons and daughters of permanent residents;<br><br>
                  (3) professionals, scientists, and artists of 'exceptional ability';<br><br>
                  (4) married adult sons and daughters of U.S. citizens;<br><br>
                  (5) siblings of adult citizens;<br><br>
                  (6) workers, skilled and unskilled, in occupations for which labor was in short supply in the United States; and<br><br>
                  (7) refugees from Communist-dominated countries or those uprooted by natural catastrophe.
               </p>

               <p><span style="font-weight:bold"> 1982: </span> Vincent Chin is beat to death by Ronald Ebens and his stepson Michael Nitz in Detroit <br>
                     &bull;&nbsp; <a href = https://en.wikipedia.org/wiki/Murder_of_Vincent_Chin> Murder of Vincent Chin </a> <br>
                     &bull;&nbsp; Ebens & Nitz were given 3 years probation, fined $3000 & never spent time in jail <br>
                     &bull;&nbsp; 1984 - Ebens is found guilty of the second count & sentenced to 25 years in prison (Nitz acquitted) <br>
                     &bull;&nbsp; 1986 - Ebens conviction is overturned due to improper coaching of prosecution witnesses <br>
                     &bull;&nbsp; 1987 - Ebens cleared of all charges in retrial that was moved to Cincinnati, Ohio. <br>

               </p>

              <p><span style="font-weight:bold"> 1986 Act: </span> Focused on curtailing illegal US immigration. It legalized hundred of thousands of illegal immigrants. The 1986 Immigration Act is commonly know as the 1986 Immigration Amnesty. It also introduced the employer sanctions program which fines employers for hiring illegal workers. It also passed tough laws to prevent bogus marriage fraud.</p>
              <p><span style="font-weight:bold"> 1990 Act: </span> Established an annual limit for certain categories of immigrants. It was aimed at helping U.S. businesses attract skilled foreign workers; thus, it expanded the business class categories to favor persons who can make educational, professional or financial contributions. It created the Immigrant Investor Program.</p>
              <p><span style="font-weight:bold"> 1992: </span>  Rodney King Riots (mainly seen as a black-white issue, but Asian Americans, particularly Korean Americans were particularly implicated/racialized).</p>
              <p><span style="font-weight:bold"> CA prop 187 1994: </span> Deny schooling and medical care to illegal immigrants</p>
              <p><span style="font-weight:bold"> USA Patriot Act 2001: </span> Uniting and Strengthening America by providing appropriate tools required to intercept and obstruct terrorism.</p>
              <p><span style="font-weight:bold"> Creation of the USCIS 2003: </span> As of March 1, 2003, the US immigration and Naturalization Service (INS) becomes part of the Department of Homeland Security (DHS). The department’s new U.S. Citizenship and US immigration Services (USCIS) function is to handle US immigration services and benefits, including citizenship, applications for permanent residence, non-immigrant applications, asylum, and refugee services. US immigration enforcement functions are now under the Department's Border and Transportation Security Directorate, known as the Bureau of US immigration and Customs Enforcement (ICE)</p>

